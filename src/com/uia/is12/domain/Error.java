package com.uia.is12.domain;

/**
 *
 * @author Gabriel
 */
public class Error {
    private int codigoError;
    private String nombreError;
    private String fechaCreacion;
    private int codigoTarea;

    public Error(){
        
    }
    
    public Error(int codigoError, String nombreError, String fechaCreacion,int codigoTarea) {
        this.codigoError = codigoError;
        this.nombreError = nombreError;
        this.fechaCreacion = fechaCreacion;
        this.codigoTarea = codigoTarea;
        
    }

    public int getCodigoTarea() {
        return codigoTarea;
    }

    public void setCodigoTarea(int codigoTarea) {
        this.codigoTarea = codigoTarea;
    }

    public int getCodigoError() {
        return codigoError;
    }

    public String getNombreError() {
        return nombreError;
    }

    public String getFechaCreacion() {
        return fechaCreacion;
    }

    public void setCodigoError(int codigoError) {
        this.codigoError = codigoError;
    }

    public void setNombreError(String nombreError) {
        this.nombreError = nombreError;
    }

    public void setFechaCreacion(String fechaCreacion) {
        this.fechaCreacion = fechaCreacion;
    }
    

    }